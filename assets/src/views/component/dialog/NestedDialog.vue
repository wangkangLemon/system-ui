<script>
    import { Dialog } from 'element-ui'

    export default {
        name: 'NestedDialog',
        mixins: [Dialog],
        props: {
            appendToBody: {
                type: Boolean,
                default: true
            }
        },
        mounted() {
            if (this.appendToBody) {
                document.body.appendChild(this.$el)
            }
        },
    }
</script>
